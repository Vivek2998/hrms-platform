  default: [
    ...PIP, ...CHIEF, ...INTERN, ...HR, ...MANAGER,
    ...ENGINEERING, ...SALES, ...FINANCE, ...MARKETING,
    ...OPERATIONS, ...CUSTOMER, ...LEGAL,
  ],
